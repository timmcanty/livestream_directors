var directors = require('./app/controllers/directors');

module.exports = function (app) {

  // Director Routes
  app.param('id', directors.load);
  app.get('/directors', directors.index);
  app.get('/directors/:id', directors.show);
  app.post('/directors', directors.create);
  // app.put('/directors/:id', directors.update);

  // catch 404 and forward to error handler
  app.use(function(req, res, next) {
    var err = new Error('Not Found');
    err.status = 404;
    next(err);
  });

  // error handlers

  // development error handler
  // will print stacktrace
  if (app.get('env') === 'development') {
    app.use(function(err, req, res, next) {
      res.status(err.status || 500);
      res.render('error', {
        message: err.message,
        error: err
      });
    });
  }

  // production error handler
  // no stacktraces leaked to user
  app.use(function(err, req, res, next) {
    res.status(err.status || 500);
    res.render('error', {
      message: err.message,
      error: {}
    });
  });
}
